#decodificar una correo que contenga "5","8","2"y"68"
import os
correo=os.sys.argv[1]

#iterar correo
for cifras in correo:
    if (cifras=="5"):
        print("j")
    if (cifras=="8"):
        print("brenis")
    if (cifras=="2"):
        print("l")
    if (cifras=="68"):
        print("gmail")
#fin_iterar

print("correo decodificado")
